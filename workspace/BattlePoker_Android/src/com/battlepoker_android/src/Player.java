package com.battlepoker_android.src;
//this is for testing.  use/modify Andrew's player class later.
public class Player {
	private String name;
	private int health;
	private int mana;
	private int x;
	private int y;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHealth() {
		return health;
	}
	public void setHealth(int health) {
		this.health = health;
	}
	public int getMana() {
		return mana;
	}
	public void setMana(int mana) {
		this.mana = mana;
	}
	public int getXPosition() {
		return x;
	}
	public void setXPosition(int xPosition) {
		this.x = xPosition;
	}
	public int getYPosition() {
		return y;
	}
	public void setYPosition(int yposition) {
		this.y = yposition;
	}

	
}
